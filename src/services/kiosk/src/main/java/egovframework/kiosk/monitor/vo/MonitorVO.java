package egovframework.kiosk.monitor.vo;

public class MonitorVO {
	private String str_code = "";
    private String call_yn = "";
    private int call_time = 0;
    private String auto_clear = "";
    
	public String getStr_code() {
		return str_code;
	}
	public void setStr_code(String str_code) {
		this.str_code = str_code;
	}
	public String getCall_yn() {
		return call_yn;
	}
	public void setCall_yn(String call_yn) {
		this.call_yn = call_yn;
	}
	public int getCall_time() {
		return call_time;
	}
	public void setCall_time(int call_time) {
		this.call_time = call_time;
	}
	public String getAuto_clear() {
		return auto_clear;
	}
	public void setAuto_clear(String auto_clear) {
		this.auto_clear = auto_clear;
	}
    
    
}
