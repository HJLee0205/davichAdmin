package egovframework.kiosk.customer.vo;

public class SimpleDataOptionVO {
	private String str_code = "";
	private String login_id = "";
	private int unit_size = 0;
	private String input_date = "";
	
	public String getStr_code() {
		return str_code;
	}
	public void setStr_code(String str_code) {
		this.str_code = str_code;
	}
	public String getLogin_id() {
		return login_id;
	}
	public void setLogin_id(String login_id) {
		this.login_id = login_id;
	}
	public int getUnit_size() {
		return unit_size;
	}
	public void setUnit_size(int unit_size) {
		this.unit_size = unit_size;
	}
	public String getInput_date() {
		return input_date;
	}
	public void setInput_date(String input_date) {
		this.input_date = input_date;
	}
}
